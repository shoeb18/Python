# String functions
name = 'python programming'

# len() returns the length of string
print(len(name))

# endswith() returns boolean
print(name.endswith('ing'))

# count() counts the string occurance
print(name.count('p'))

# capitalize() capitals first letter
print(name.capitalize())

# find() returns the string first occurance index if present if not returns -1
print(name.find('p'))

# replace() replace old --> new in all occurances
print(name.replace('python', 'java'))