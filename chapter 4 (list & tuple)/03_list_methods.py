# List methods
numbers = [1,32,3,44,35]

# sort() sort the list in ascending order
numbers.sort()

# reverse() sort the list in descending order
numbers.reverse()

# append() adds 89 at the end of the list
numbers.append(89)

# insert() adds 98 at the index 2
numbers.insert(2,98)

# pop() remove item at index 3
numbers.pop(3)

# remove() remove item with value
numbers.remove(98)

print(numbers)